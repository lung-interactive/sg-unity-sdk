namespace SGUnitySDK
{
    /// <summary>
    /// Defines the serialized value types supported by additional action metadata.
    /// </summary>
    public enum AdditionalMetadataValueType
    {
        /// <summary>
        /// Represents a text value.
        /// </summary>
        String = 1,

        /// <summary>
        /// Represents a 32-bit integer value.
        /// </summary>
        Integer = 2,

        /// <summary>
        /// Represents a single-precision floating-point value.
        /// </summary>
        Float = 3,

        /// <summary>
        /// Represents a boolean value.
        /// </summary>
        Boolean = 4,
    }
}